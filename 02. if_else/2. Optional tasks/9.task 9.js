var a = 3;
var b = 4;
var c = 5;

if ((a + b) > c && (a + c) > b && (b + c) > a) {
    console.log(`Числата могат да бъдат дължини на триъгълник`);
}

/*
Формула
a + b > c
a + c > b
b + c > a

Случай при който числата могат да бъдат дължини на триъгълник
a = 3
b = 4
c = 5

a + b = 7
a + c = 8 
b + c = 9
*/